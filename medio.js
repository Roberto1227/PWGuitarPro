  function showModal(event) {
    if (currentAudio) {
      currentAudio.pause();
      currentAudio.currentTime = 0;
    }
  
    const lessonType = event.target.getAttribute("data-modal");
    console.log("Botón presionado:", lessonType);
  
    if (lessonType === "acordes7Mayores") {
      chords = acordes7Mayores;
    } else if (lessonType === "acordes7Menores") {
      chords = acordes7Menores;
    } else if (lessonType === "acordes6") {
      chords = acordes6;
    } else if (lessonType === "acordesSuspendidos2") {
      chords = acordesSuspendidos2;
    } else if (lessonType === "acordesSuspendidos4") {
      chords = acordesSuspendidos4;
    } else if (lessonType === "acordesDisminuidos") {
      chords = acordesDisminuidos;
    } else if (lessonType === "acordesAumentados") {
      chords = acordesAumentados;
    } else {
      console.error("Lección no encontrada: ", lessonType);
      return;
    }
  
    currentChordIndex = 0;
    lessonModal.classList.remove("hidden");
    introSlide.style.display = "block";
    chordSlide.style.display = "none";
    finalSlide.style.display = "none";
  
    const lessonNameText = lessonType.includes("Suspendidos")
      ? `Lección: ${lessonType.replace("acordes", "").replace(/\d/g, "").trim()}`
      : `Lección: ${lessonType}`;
  
    document.getElementById("lessonName").textContent = lessonNameText;
  }
